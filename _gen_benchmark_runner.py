"""Generates benchmark_runner.ipynb — runs all benchmark methods and saves results to JSON.

Methods: Full Graph, Static PPR, Static k-hop, Learnable PPR.
Results are written to results/benchmark-comparison/ as JSON for the analysis notebook.
"""
import json

nb = {
    "cells": [],
    "metadata": {
        "kernelspec": {"display_name": "Python 3", "language": "python", "name": "python3"},
        "language_info": {"name": "python", "version": "3.13.0"}
    },
    "nbformat": 4,
    "nbformat_minor": 4
}

def md(src):
    nb["cells"].append({"cell_type": "markdown", "metadata": {}, "source": [src]})

def code(src):
    nb["cells"].append({"cell_type": "code", "execution_count": None, "metadata": {}, "outputs": [], "source": [src]})


# ═══════════════════════════════════════════════════════════════════════════════
md(
"# Benchmark Runner\n"
"\n"
"Runs all link-prediction methods and saves per-experiment JSON results.\n"
"**Do not** use this notebook for visualization — see `benchmark_analysis.ipynb`.\n"
"\n"
"| Method | Subgraph | Sweep |\n"
"|--------|----------|-------|\n"
"| Full Graph | None | — |\n"
"| Static PPR | Top-k PPR | `PPR_TOPK` |\n"
"| Static k-hop | k-hop neighborhood | `K_VALUES` |\n"
"| Learnable PPR | Learned per-edge | — |"
)

# ── Config ────────────────────────────────────────────────────────────────────
code(
"# ═══════════════════════════════════════════════════════════════════════════════\n"
"# CONFIGURATION\n"
"# ═══════════════════════════════════════════════════════════════════════════════\n"
"\n"
"SEED = 42\n"
"DATASETS  = ['FB15K237']           # FB15K237, WN18RR, NELL-995\n"
"ENCODERS  = ['GCN', 'SAGE', 'GAT']\n"
"\n"
"# Shared architecture\n"
"FEATURE_DIM     = 128\n"
"HIDDEN_CHANNELS = 256\n"
"NUM_LAYERS      = 3\n"
"DROPOUT         = 0.3\n"
"\n"
"# Full-graph training\n"
"FULL_EPOCHS     = 500\n"
"FULL_BATCH_SIZE = 65536\n"
"FULL_LR         = 0.005\n"
"FULL_PATIENCE   = 30\n"
"\n"
"# Subgraph-based training (PPR, k-hop)\n"
"SUB_EPOCHS      = 500\n"
"SUB_BATCH_SIZE  = 8192\n"
"SUB_LR          = 0.005\n"
"SUB_PATIENCE    = 30\n"
"EVAL_STEPS      = 5\n"
"WEIGHT_DECAY    = 1e-5\n"
"GRAD_CLIP       = 1.0\n"
"\n"
"# Static PPR sweep\n"
"PPR_ALPHA     = 0.85\n"
"PPR_TOPK      = [50, 100, 200, 300, 500]\n"
"ALPHA_COMBINE = 0.5\n"
"\n"
"# Static k-hop sweep\n"
"K_VALUES = [1, 2, 3]\n"
"\n"
"# Learnable PPR\n"
"TELEPORT_VALUES          = [0.50, 0.85, 0.95]\n"
"SEARCH_EPOCHS            = 50\n"
"SEARCH_BATCH_SIZE        = 1024\n"
"SEARCH_LR                = 0.01\n"
"TEMPERATURE_START        = 1.0\n"
"TEMPERATURE_END          = 0.2\n"
"EDGES_PER_SEARCH_EPOCH   = 100_000\n"
"FIRST_ORDER              = True\n"
"SEARCH_VAL_EVERY         = 5\n"
"USE_AMP                  = True\n"
"LEARNABLE_TOP_K          = 100\n"
"FINETUNE_EPOCHS          = 200\n"
"FINETUNE_BATCH_SIZE      = 8192\n"
"FINETUNE_LR              = 0.005\n"
"FINETUNE_PATIENCE        = 20\n"
"MAX_SUBGRAPHS_PER_FORWARD = 256\n"
"EDGES_PER_EPOCH          = 100_000\n"
"\n"
"# Output directory (JSON per experiment)\n"
"SAVE_DIR = 'results/benchmark-comparison'\n"
"\n"
"import torch\n"
"DEVICE = 'cuda' if torch.cuda.is_available() else 'cpu'\n"
"print(f'Device: {DEVICE}  |  Datasets: {DATASETS}  |  Encoders: {ENCODERS}')"
)

# ── Imports + seed ────────────────────────────────────────────────────────────
code(
"import numpy as np, random, json, os, time\n"
"\n"
"torch.manual_seed(SEED)\n"
"torch.cuda.manual_seed_all(SEED)\n"
"np.random.seed(SEED)\n"
"random.seed(SEED)\n"
"torch.backends.cudnn.deterministic = True\n"
"torch.backends.cudnn.benchmark = False\n"
"\n"
"from subgrapher.benchmark.data_prep import prepare_link_prediction_data\n"
"from subgrapher.utils.models import GCN, SAGE, GAT, LinkPredictor\n"
"\n"
"os.makedirs(SAVE_DIR, exist_ok=True)\n"
"\n"
"DATASET_PATHS = {\n"
"    'FB15K237':  'data/FB15K237/train.txt',\n"
"    'WN18RR':    'data/WN18RR/train.txt',\n"
"    'NELL-995':  'data/NELL-995/train.txt',\n"
"}\n"
"\n"
"def make_encoder(enc_type, in_ch=FEATURE_DIM, hid=HIDDEN_CHANNELS,\n"
"                 n_layers=NUM_LAYERS, dropout=DROPOUT):\n"
"    if enc_type == 'GCN':  return GCN(in_ch, hid, hid, n_layers, dropout)\n"
"    if enc_type == 'SAGE': return SAGE(in_ch, hid, hid, n_layers, dropout)\n"
"    if enc_type == 'GAT':  return GAT(in_ch, hid, hid, n_layers, dropout, heads=4)\n"
"    raise ValueError(enc_type)\n"
"\n"
"def make_predictor(hid=HIDDEN_CHANNELS, n_layers=NUM_LAYERS, dropout=DROPOUT):\n"
"    return LinkPredictor(hid, hid, 1, n_layers, dropout)\n"
"\n"
"def save_result(entry):\n"
"    \"\"\"Append one experiment result to the combined JSON file.\"\"\"\n"
"    path = os.path.join(SAVE_DIR, 'all_results.json')\n"
"    results = []\n"
"    if os.path.exists(path):\n"
"        with open(path) as f:\n"
"            results = json.load(f)\n"
"    results.append(entry)\n"
"    with open(path, 'w') as f:\n"
"        json.dump(results, f, indent=2, default=str)\n"
"    print(f'  -> Saved ({len(results)} total results)')\n"
"\n"
"print('Ready.')"
)

# ── Data loading ──────────────────────────────────────────────────────────────
md("## 1. Load Datasets")

code(
"datasets = {}\n"
"for ds_name in DATASETS:\n"
"    print(f'\\nLoading {ds_name}...')\n"
"    dd = prepare_link_prediction_data(\n"
"        DATASET_PATHS[ds_name],\n"
"        feature_method='random', feature_dim=FEATURE_DIM)\n"
"    datasets[ds_name] = dd\n"
"    data = dd['data']; se = dd['split_edge']\n"
"    print(f'  Nodes: {data.num_nodes:,}  Edges: {data.edge_index.size(1):,}')\n"
"    print(f'  Train: {se[\"train\"][\"source_node\"].size(0):,}  '\n"
"          f'Val: {se[\"valid\"][\"source_node\"].size(0):,}  '\n"
"          f'Test: {se[\"test\"][\"source_node\"].size(0):,}')"
)

# ── Full Graph ────────────────────────────────────────────────────────────────
md("## 2. Full Graph (No Subgraph)")

code(
"from subgrapher.benchmark.trainer import benchmark_model\n"
"\n"
"for ds_name in DATASETS:\n"
"    dd = datasets[ds_name]\n"
"    data = dd['data'].clone()\n"
"    data.edge_index = dd['train_edge_index']\n"
"    split_edge = dd['split_edge']\n"
"\n"
"    for enc_type in ENCODERS:\n"
"        print(f'\\n=== Full Graph: {ds_name} / {enc_type} ===')\n"
"        torch.manual_seed(SEED)\n"
"        encoder   = make_encoder(enc_type)\n"
"        predictor = make_predictor()\n"
"        encoder.reset_parameters()\n"
"        predictor.reset_parameters()\n"
"\n"
"        result = benchmark_model(\n"
"            enc_type, encoder, predictor, data, split_edge,\n"
"            epochs=FULL_EPOCHS, batch_size=FULL_BATCH_SIZE,\n"
"            lr=FULL_LR, eval_steps=EVAL_STEPS, device=DEVICE,\n"
"            patience=FULL_PATIENCE, weight_decay=WEIGHT_DECAY,\n"
"            grad_clip=GRAD_CLIP)\n"
"\n"
"        save_result({\n"
"            'method': 'Full Graph', 'dataset': ds_name,\n"
"            'encoder': enc_type, 'config': '-',\n"
"            'config_value': None,\n"
"            **{k: float(v) for k, v in result['test_results'].items()},\n"
"            'train_time': result['train_time'],\n"
"            'best_epoch': result['best_epoch'],\n"
"            'stopped_early': result['stopped_early'],\n"
"            'seed': SEED,\n"
"        })"
)

# ── Static PPR ────────────────────────────────────────────────────────────────
md("## 3. Static PPR Subgraph")

code(
"from subgrapher.benchmark_ppr.run_ppr_benchmark import load_or_create_ppr_preprocessor\n"
"from subgrapher.benchmark_ppr.ppr_extractor import StaticPPRExtractor\n"
"from subgrapher.benchmark_ppr.trainer_batched import train_model_ppr_batched\n"
"from subgrapher.benchmark_ppr.evaluator import evaluate_ppr\n"
"\n"
"for ds_name in DATASETS:\n"
"    dd = datasets[ds_name]\n"
"    data = dd['data']; split_edge = dd['split_edge']\n"
"    ppr_pre = load_or_create_ppr_preprocessor(ds_name, data, PPR_ALPHA)\n"
"\n"
"    for top_k in PPR_TOPK:\n"
"        ppr_ext = StaticPPRExtractor(\n"
"            data, alpha=ALPHA_COMBINE, top_k=top_k,\n"
"            ppr_alpha=PPR_ALPHA, preprocessor=ppr_pre)\n"
"\n"
"        for enc_type in ENCODERS:\n"
"            print(f'\\n=== Static PPR (top_k={top_k}): {ds_name} / {enc_type} ===')\n"
"            torch.manual_seed(SEED)\n"
"            encoder   = make_encoder(enc_type)\n"
"            predictor = make_predictor()\n"
"            encoder.reset_parameters()\n"
"            predictor.reset_parameters()\n"
"\n"
"            t0 = time.time()\n"
"            hist = train_model_ppr_batched(\n"
"                encoder, predictor, data, split_edge, ppr_ext,\n"
"                epochs=SUB_EPOCHS, batch_size=SUB_BATCH_SIZE,\n"
"                lr=SUB_LR, eval_steps=EVAL_STEPS, device=DEVICE,\n"
"                verbose=True, patience=SUB_PATIENCE,\n"
"                weight_decay=WEIGHT_DECAY, grad_clip=GRAD_CLIP)\n"
"            train_time = time.time() - t0\n"
"\n"
"            test_res = evaluate_ppr(\n"
"                encoder, predictor, data, split_edge, ppr_ext,\n"
"                split='test', batch_size=SUB_BATCH_SIZE, device=DEVICE)\n"
"\n"
"            save_result({\n"
"                'method': 'Static PPR', 'dataset': ds_name,\n"
"                'encoder': enc_type,\n"
"                'config': f'top_k={top_k}',\n"
"                'config_value': top_k,\n"
"                'ppr_alpha': PPR_ALPHA,\n"
"                **{k: float(v) for k, v in test_res.items()},\n"
"                'train_time': train_time,\n"
"                'best_epoch': hist.get('best_epoch', 0),\n"
"                'stopped_early': hist.get('stopped_early', False),\n"
"                'seed': SEED,\n"
"            })"
)

# ── Static k-hop ─────────────────────────────────────────────────────────────
md("## 4. Static k-hop Subgraph")

code(
"from subgrapher.benchmark_khop.run_khop_benchmark import load_or_create_khop_preprocessor\n"
"from subgrapher.benchmark_khop.khop_extractor import StaticKHopExtractor\n"
"from subgrapher.benchmark_khop.trainer_batched import train_model_khop_batched\n"
"from subgrapher.benchmark_khop.evaluator import evaluate_khop\n"
"\n"
"for ds_name in DATASETS:\n"
"    dd = datasets[ds_name]\n"
"    data = dd['data']; split_edge = dd['split_edge']\n"
"\n"
"    for k in K_VALUES:\n"
"        khop_pre = load_or_create_khop_preprocessor(ds_name, data, k)\n"
"        khop_ext = StaticKHopExtractor(data, k=k, preprocessor=khop_pre)\n"
"\n"
"        for enc_type in ENCODERS:\n"
"            print(f'\\n=== Static k-hop (k={k}): {ds_name} / {enc_type} ===')\n"
"            torch.manual_seed(SEED)\n"
"            encoder   = make_encoder(enc_type)\n"
"            predictor = make_predictor()\n"
"            encoder.reset_parameters()\n"
"            predictor.reset_parameters()\n"
"\n"
"            t0 = time.time()\n"
"            hist = train_model_khop_batched(\n"
"                encoder, predictor, data, split_edge, khop_ext,\n"
"                epochs=SUB_EPOCHS, batch_size=SUB_BATCH_SIZE,\n"
"                lr=SUB_LR, eval_steps=EVAL_STEPS, device=DEVICE,\n"
"                verbose=True, patience=SUB_PATIENCE,\n"
"                weight_decay=WEIGHT_DECAY, grad_clip=GRAD_CLIP)\n"
"            train_time = time.time() - t0\n"
"\n"
"            test_res = evaluate_khop(\n"
"                encoder, predictor, data, split_edge, khop_ext,\n"
"                split='test', batch_size=SUB_BATCH_SIZE, device=DEVICE)\n"
"\n"
"            save_result({\n"
"                'method': 'Static k-hop', 'dataset': ds_name,\n"
"                'encoder': enc_type,\n"
"                'config': f'k={k}',\n"
"                'config_value': k,\n"
"                **{k_: float(v) for k_, v in test_res.items()},\n"
"                'train_time': train_time,\n"
"                'best_epoch': hist.get('best_epoch', 0),\n"
"                'stopped_early': hist.get('stopped_early', False),\n"
"                'seed': SEED,\n"
"            })"
)

# ── Learnable PPR ─────────────────────────────────────────────────────────────
md("## 5. Learnable PPR (Architecture Search + Fine-tune)")

code(
"from subgrapher.benchmark_learnable_ppr.multi_scale_ppr import MultiScalePPR\n"
"from subgrapher.benchmark_learnable_ppr.autolink_ppr import AutoLinkPPR\n"
"from subgrapher.benchmark_learnable_ppr.search_net import PPRSearchNet\n"
"from subgrapher.benchmark_learnable_ppr.searcher import ArchitectureSearcher\n"
"from subgrapher.benchmark_learnable_ppr.finetuner import finetune_on_subgraphs\n"
"from subgrapher.benchmark_learnable_ppr.evaluator import evaluate_learnable_ppr\n"
"\n"
"for ds_name in DATASETS:\n"
"    dd = datasets[ds_name]\n"
"    data = dd['data']; split_edge = dd['split_edge']\n"
"    msp = MultiScalePPR(\n"
"        ds_name, data, teleport_values=TELEPORT_VALUES, device=DEVICE)\n"
"    num_configs = msp.num_configs\n"
"\n"
"    for enc_type in ENCODERS:\n"
"        print(f'\\n=== Learnable PPR: {ds_name} / {enc_type} ===')\n"
"        torch.manual_seed(SEED)\n"
"\n"
"        # Phase 1\n"
"        model = AutoLinkPPR(\n"
"            in_channels=FEATURE_DIM, hidden_channels=HIDDEN_CHANNELS,\n"
"            num_layers=NUM_LAYERS, dropout=DROPOUT,\n"
"            gnn_type=enc_type, num_configs=num_configs)\n"
"        arch_net = PPRSearchNet(\n"
"            in_channels=HIDDEN_CHANNELS, hidden_channels=256,\n"
"            num_layers=3, temperature=TEMPERATURE_START)\n"
"\n"
"        searcher = ArchitectureSearcher(\n"
"            model, arch_net, msp, data, split_edge,\n"
"            device=DEVICE, lr=SEARCH_LR, lr_arch=SEARCH_LR,\n"
"            temperature_start=TEMPERATURE_START,\n"
"            temperature_end=TEMPERATURE_END,\n"
"            edges_per_search_epoch=EDGES_PER_SEARCH_EPOCH,\n"
"            first_order=FIRST_ORDER)\n"
"\n"
"        search_hist = searcher.search(\n"
"            epochs=SEARCH_EPOCHS, batch_size=SEARCH_BATCH_SIZE,\n"
"            verbose=True, val_every=SEARCH_VAL_EVERY, use_amp=USE_AMP)\n"
"\n"
"        train_configs, train_counts = searcher.get_edge_configs('train')\n"
"        val_configs, _   = searcher.get_edge_configs('valid')\n"
"        test_configs, _  = searcher.get_edge_configs('test')\n"
"\n"
"        # Phase 2\n"
"        ft_encoder   = make_encoder(enc_type)\n"
"        ft_predictor = make_predictor()\n"
"        ckpt_dir  = f'checkpoints/learnable-ppr/{ds_name}/{enc_type}'\n"
"        cache_dir = f'cache/learnable-ppr/{ds_name}/{enc_type}'\n"
"\n"
"        ft_hist = finetune_on_subgraphs(\n"
"            ft_encoder, ft_predictor, data, split_edge,\n"
"            msp, train_configs,\n"
"            alpha=ALPHA_COMBINE, top_k=LEARNABLE_TOP_K,\n"
"            epochs=FINETUNE_EPOCHS, batch_size=FINETUNE_BATCH_SIZE,\n"
"            lr=FINETUNE_LR, eval_steps=EVAL_STEPS, device=DEVICE,\n"
"            verbose=True, patience=FINETUNE_PATIENCE,\n"
"            max_subgraphs_per_forward=MAX_SUBGRAPHS_PER_FORWARD,\n"
"            checkpoint_dir=ckpt_dir, cache_dir=cache_dir,\n"
"            edges_per_epoch=EDGES_PER_EPOCH,\n"
"            val_config_indices=val_configs)\n"
"\n"
"        test_res = evaluate_learnable_ppr(\n"
"            ft_encoder, ft_predictor, data, split_edge,\n"
"            msp, test_configs,\n"
"            split='test', alpha=ALPHA_COMBINE, top_k=LEARNABLE_TOP_K,\n"
"            device=DEVICE, cache_dir=cache_dir)\n"
"\n"
"        total_time = search_hist['total_time'] + ft_hist.get('total_time', 0)\n"
"\n"
"        save_result({\n"
"            'method': 'Learnable PPR', 'dataset': ds_name,\n"
"            'encoder': enc_type,\n"
"            'config': f'top_k={LEARNABLE_TOP_K}',\n"
"            'config_value': LEARNABLE_TOP_K,\n"
"            'teleport_values': TELEPORT_VALUES,\n"
"            'search_time': search_hist['total_time'],\n"
"            'finetune_time': ft_hist.get('total_time', 0),\n"
"            'train_config_counts': train_counts.tolist(),\n"
"            **{k_: float(v) for k_, v in test_res.items()},\n"
"            'train_time': total_time,\n"
"            'best_epoch': ft_hist.get('best_epoch', 0),\n"
"            'stopped_early': ft_hist.get('stopped_early', False),\n"
"            'seed': SEED,\n"
"        })"
)

